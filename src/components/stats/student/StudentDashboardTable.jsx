export default function StudentDashboardTable() {
  const [order, setOrder] = React.useState("asc");
  const [orderBy, setOrderBy] = React.useState("calories");
  const [selected, setSelected] = React.useState([]);
  const [page, setPage] = React.useState(0);
  const [dense, setDense] = React.useState(false);
  const [rowsPerPage, setRowsPerPage] = React.useState(5);

  const { users } = useSelector((state) => state.info);
  const { loggedUser } = useSelector((state) => state.auth);
  const navigate = useNavigate();

  const goToStudentDetails = (id) => {
    navigate(`/home/students/details/${id}`);
  };

  // const students = users.filter((u) => u.role === "student");
  // console.log("students", students);
  // const loggedStudent = students.find((s) => s.uid === loggedUser.uid);
  const studentsData = loggedUser?.enrolledCourse?.map((s) => {
    const courseName = s.courseName;

    let downloadedAssignmentsCount = 0;

    s.assignmentDetails.forEach((ad) => {
      if (ad?.isAssignmentDownloaded) {
        downloadedAssignmentsCount += 1;
      }
    });

    let uploadedAssignmentsCount = 0;

    s.assignmentDetails.forEach((ad) => {
      if (ad?.isAssignmentUploaded) {
        uploadedAssignmentsCount += 1;
      }
    });

    let checkedAssignmentsCount = 0;

    s.assignmentDetails.forEach((ad) => {
      if (ad?.isChecked) {
        checkedAssignmentsCount += 1;
      }
    });

    return {
      courseName,
      checkedAssignmentsCount,
      downloadedAssignmentsCount,
      uploadedAssignmentsCount,
    };
  });

  console.log("studetsdata", studentsData);

  const handleRequestSort = (event, property) => {
    const isAsc = orderBy === property && order === "asc";
    setOrder(isAsc ? "desc" : "asc");
    setOrderBy(property);
  };

  const handleSelectAllClick = (event) => {
    if (event.target.checked) {
      const newSelected = studentsData.map((n) => n.courseName);
      setSelected(newSelected);
      return;
    }
    setSelected([]);
  };

  const handleClick = (event, courseName) => {
    const selectedIndex = selected.indexOf(courseName);
    let newSelected = [];

    if (selectedIndex === -1) {
      newSelected = newSelected.concat(selected, courseName);
    } else if (selectedIndex === 0) {
      newSelected = newSelected.concat(selected.slice(1));
    } else if (selectedIndex === selected.length - 1) {
      newSelected = newSelected.concat(selected.slice(0, -1));
    } else if (selectedIndex > 0) {
      newSelected = newSelected.concat(
        selected.slice(0, selectedIndex),
        selected.slice(selectedIndex + 1)
      );
    }

    setSelected(newSelected);
  };

  const handleChangePage = (event, newPage) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event) => {
    setRowsPerPage(parseInt(event.target.value, 10));
    setPage(0);
  };

  const handleChangeDense = (event) => {
    setDense(event.target.checked);
  };

  const isSelected = (courseName) => selected.indexOf(courseName) !== -1;

  // Avoid a layout jump when reaching the last page with empty rows.
  const emptyRows =
    page > 0 ? Math.max(0, (1 + page) * rowsPerPage - studentsData.length) : 0;

  return (
    <Box sx={{ width: "100%" }}>
      <Paper sx={{ width: "100%", mb: 2 }}>
        <EnhancedTableToolbar numSelected={selected.length} />
        <TableContainer>
          <Table
            sx={{ minWidth: 750 }}
            aria-labelledby="tableTitle"
            size={dense ? "small" : "medium"}
          >
            <EnhancedTableHead
              numSelected={selected.length}
              order={order}
              orderBy={orderBy}
              onSelectAllClick={handleSelectAllClick}
              onRequestSort={handleRequestSort}
              rowCount={studentsData?.length}
            />
            <TableBody>
              {/* if you don't need to support IE11, you can replace the `stableSort` call with:
                 rows.slice().sort(getComparator(order, orderBy)) */}
              {stableSort(studentsData, getComparator(order, orderBy))
                .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
                .map((row, index) => {
                  const isItemSelected = isSelected(row.courseName);
                  const labelId = `enhanced-table-checkbox-${index}`;

                  return (
                    <TableRow
                      hover
                      onClick={(event) => handleClick(event, row.courseName)}
                      role="checkbox"
                      aria-checked={isItemSelected}
                      tabIndex={-1}
                      key={row.courseName}
                      selected={isItemSelected}
                    >
                      {/* <TableCell padding="checkbox">
                        <Checkbox
                          color="primary"
                          checked={isItemSelected}
                          inputProps={{
                            "aria-labelledby": labelId
                          }}
                        />
                      </TableCell> */}
                      <TableCell
                        component="th"
                        id={labelId}
                        scope="row"
                        padding="normal"
                        sx={{ textTransform: "capitalize", padding: "0px 5px" }}
                      >
                        {row.courseName}
                      </TableCell>
                      <TableCell align="center">
                        {row.downloadedAssignmentsCount}
                      </TableCell>
                      <TableCell align="center">
                        {row.uploadedAssignmentsCount}
                      </TableCell>
                      <TableCell align="center">
                        {row.checkedAssignmentsCount}
                      </TableCell>
                      {/* <TableCell align="right">{row.active}</TableCell> */}
                      {/* <TableCell>
                        <button className="btn" onClick={()=> goToStudentDetails(row.id)}>View Details</button>
                      </TableCell> */}
                    </TableRow>
                  );
                })}
              {emptyRows > 0 && (
                <TableRow
                  style={{
                    height: (dense ? 33 : 53) * emptyRows,
                  }}
                >
                  <TableCell colSpan={6} />
                </TableRow>
              )}
            </TableBody>
          </Table>
        </TableContainer>
        <TablePagination
          rowsPerPageOptions={[5, 10, 25]}
          component="div"
          count={studentsData.length}
          rowsPerPage={rowsPerPage}
          page={page}
          onPageChange={handleChangePage}
          onRowsPerPageChange={handleChangeRowsPerPage}
        />
      </Paper>
    </Box>
  );
}
